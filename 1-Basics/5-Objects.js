// REFERENCE TYPES
// When we are dealing with multiple variables so we can put into the objects.

let person = {
    name: 'Noor',
    age: 30
};

console.log(person)

// Changing the name with dot notation
person.name = 'John';
console.log(person.name)

// Other way to chang the value is [Bracket Notation]
person['name'] = 'Mary'

console.log(person.name)

